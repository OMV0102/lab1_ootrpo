#include <iostream>
#include "PRNG.h"

void main()
{
    PRNG prng;
    vector<double> sequence;
    string errorMessage = "";
    int number = 10;
    bool result = prng.GetSequence(number, &sequence, &errorMessage);
    if(result == false)
        cout << errorMessage << endl;
    else
        for(int i = 0; i < number; i++)
        {
            cout << sequence[i] << endl;

        }
}

